// Write a program with bitwise operators
public class bitwiseOperator {
    public static void main(String[] args)
    {
        int a=5;
        int b=4;
        
        System.out.println("the first Method is ARE Operator :"+(a|b));
        
        System.out.println("the first Method is AND Operator :" + (a&b));

        System.out.println("the first Method is XOR Operator :"+(a^b));

        System.out.println("the first Method is leftShift Operator :"+(a<<b));
        
        System.out.println("the first Method is RightShift Operator :"+(a>>b));
       

    }
}
